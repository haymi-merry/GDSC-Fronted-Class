function sum(num) {
  var sum = 0; // Initialize sum to 0
  for (let i = 1; i <= num; i++) {
    sum += i; // Add each number to sum
  }
  console.log(sum); // Print the result
}



sum(10); // summation: 55
sum(20);  // summation: 210
sum(23);   // summation: 276
sum(100);   // summation: 5050
sum(650); // summation: 211575
